# Ввод данных
set1 = set(map(int, input("Введите элементы первого множества через пробел: ").split()))
set2 = set(map(int, input("Введите элементы второго множества через пробел: ").split()))
# Нахождение всех различных чисел в обоих множествах
unique_nums = set1.union(set2)
print("Все различные числа в обоих множествах:", unique_nums)
# Нахождение чисел, входящих в оба множества, и их сортировка в порядке возрастания
common_nums = sorted(set1.intersection(set2))
print("Числа, входящие как в первое, так и во второе множества:", common_nums)